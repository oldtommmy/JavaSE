package com.tommychan.javaselearning.tankgame01;
/**
* @author TommyChan
* @version 1.0
* description: tank
*/
public class Tank {
    private int x;
    private int y;

    public Tank(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
